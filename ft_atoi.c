#include "libft.h"

int	return_long(int neg)
{
	if (neg == 1)
		return ((int)(LONG_MAX));
	else
		return ((int)(LONG_MIN));
}

int	ft_atoi(const char *nptr)
{
	int		i;
	int		neg;
	long	nb;

	i = 0;
	neg = 1;
	nb = 0;
	while (nptr[i] == '\t' || nptr[i] == '\n' || nptr[i] == '\v' || \
			nptr[i] == '\f' || nptr[i] == '\r' || nptr[i] == ' ')
		i++;
	while (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
			neg *= -1;
		i++;
	}
	while ((nptr[i] >= 48) && (nptr[i] <= 57))
	{
		nb = nb * 10 + (nptr[i] - 48);
		if (nb < 0 || (i > 0 && nb == 0))
			return (return_long(neg));
		i++;
	}
	return ((int)(nb * neg));
}

